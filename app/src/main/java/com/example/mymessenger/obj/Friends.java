package com.example.mymessenger.obj;

public class Friends {
    public String id_user0;
    public String id_user1;
    public Boolean stt_user0;
    public Boolean stt_user1;
    public String time;

    public Friends() {
    }

    public Friends(String id_user0, String id_user1, Boolean stt_user0, Boolean stt_user1, String time) {
        this.id_user0 = id_user0;
        this.id_user1 = id_user1;
        this.stt_user0 = stt_user0;
        this.stt_user1 = stt_user1;
        this.time = time;
    }
}
